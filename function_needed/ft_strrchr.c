#include <stddef.h>

char	*strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
		i++;
	i--;
	while (s[i] != c && i >= 0)
		i--;
	if (i == -1)
		return (NULL);
	else
		return ((char *)(s + i));
}
